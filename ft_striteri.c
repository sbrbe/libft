#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	len;
	size_t	i;

	i = 0;
	len = ft_strlen(s);
	s[len] = '\0';
	while (s[i] != '\0')
	{
		(*f)((unsigned int)i, &s[i]);
		i++;
	}
}
