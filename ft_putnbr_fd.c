#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	nb;

	if (n == -2147483648)
	{
		n = 8;
		write (fd, "-214748364", 10);
	}
	if (n < 0)
	{
		write (fd, "-", 1);
		n = n * -1;
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	nb = n % 10 + '0';
	write (fd, &nb, 1);
}
